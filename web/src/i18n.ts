const normalizeLanguage = (lang?: string | null): SupportedLanguage => {
  if (!lang) return 'zh-CN';
  const lower = lang.toLowerCase();
  if (lower.startsWith('en')) return 'en';
  if (lower.startsWith('zh-tw') || lower.startsWith('zh-hk') || lower.startsWith('zh-mo') || lower.includes('hant')) {
    return 'zh-TW';
  }
  return 'zh-CN';
};

const savedLang = localStorage.getItem('mh_lang');
const initialLanguage = normalizeLanguage(savedLang || (typeof navigator !== 'undefined' ? navigator.language : 'zh-CN'));

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: initialLanguage,
    fallbackLng: 'zh-CN',
    supportedLngs: ['en', 'zh-CN', 'zh-TW'],
    interpolation: { escapeValue: false },
  })
  .catch(() => null);

export const setLanguage = (lang: SupportedLanguage) => {
  i18n.changeLanguage(lang);
  localStorage.setItem('mh_lang', lang);
};

export default i18n;
